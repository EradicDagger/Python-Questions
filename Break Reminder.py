#This program Remindes us of taking a break after every 2 hrs from the start of execution
import webbrowser
import time

total_breaks=3
break_count=0
print("Start time - "+time.ctime())
while(break_count < total_breaks):
    time.sleep(2*3600)
    webbrowser.open("https://www.youtube.com/watch?v=2nDGVLPdlIk")
    break_count= break_count + 1
